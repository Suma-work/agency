package com.sumainfo.common.entity;



public class SmsReturn {
	
	private int id;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * 上行时间
	 */
	private String moTime;
	/**
	 * 平台通道码
	 */
	private String spCode;
	/**
	 * 上行号码
	 */
	private String mobile;
	/**
	 * 运营商通道码
	 */
	private String destCode;
	/**
	 * 上行内容
	 */
	private String messageContent;
	
	
	
	public SmsReturn() {
		super();
	}
	
	
	public SmsReturn(String moTime, String spCode, String mobile,
			String destCode, String messageContent) {
		super();
		this.moTime = moTime;
		this.spCode = spCode;
		this.mobile = mobile;
		this.destCode = destCode;
		this.messageContent = messageContent;
	}
	
	
	public String getMoTime() {
		return moTime;
	}
	public void setMoTime(String moTime) {
		this.moTime = moTime;
	}
	public String getSpCode() {
		return spCode;
	}
	public void setSpCode(String spCode) {
		this.spCode = spCode;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getDestCode() {
		return destCode;
	}
	public void setDestCode(String destCode) {
		this.destCode = destCode;
	}
	public String getMessageContent() {
		return messageContent;
	}
	public void setMessageContent(String messageContent) {
		this.messageContent = messageContent;
	}
	@Override
	public String toString() {
		return "SmsReturn [moTime=" + moTime + ", spCode=" + spCode
				+ ", mobile=" + mobile + ", destCode=" + destCode
				+ ", messageContent=" + messageContent + "]";
	}
	
	

}
