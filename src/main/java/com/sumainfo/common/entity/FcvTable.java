package com.sumainfo.common.entity;


/**
 * 一级车辆信息表
 * @author DYang
 *
 */
public class FcvTable {
	
	//主键id
	private int fcvId;
	//车辆id
	private int id;
	//车辆名称
	private String name;
	//初始级
	private String initial;
	//车辆图标
	private String logo;
	
	
	public FcvTable() {
	}

	public FcvTable(int id, String name, String initial,
			String logo) {
		this.id = id;
		this.name = name;
		this.initial = initial;
		this.logo = logo;
	}

	public int getFcvId() {
		return fcvId;
	}

	public void setFcvId(int fcvId) {
		this.fcvId = fcvId;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getInitial() {
		return initial;
	}

	public void setInitial(String initial) {
		this.initial = initial;
	}


	public String getLogo() {
		return logo;
	}

	public void setLogo(String logo) {
		this.logo = logo;
	}



	
	
	
	

}
