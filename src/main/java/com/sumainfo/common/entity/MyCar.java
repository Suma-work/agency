package com.sumainfo.common.entity;

import java.util.Date;

public class MyCar {
	private int id;
	private int cusId;
	private String picAdress;
	private String carTypeName;
	private String carNo;
	private Date createTime;
	private String isDefault;
	private String recognitionNo;
	private String carEngineNo;

	
	public MyCar() {
		super();
	}

	
	public MyCar(int cusId, String picAdress, String carTypeName, String carNo,
			Date createTime, String isDefault,String recognitionNo,String carEngineNo) {
		super();
		this.cusId = cusId;
		this.picAdress = picAdress;
		this.carTypeName = carTypeName;
		this.carNo = carNo;
		this.createTime = createTime;
		this.isDefault = isDefault;
		this.recognitionNo = recognitionNo;
		this.carEngineNo = carEngineNo;
	}
	
	public MyCar(String picAdress, String carTypeName, String carNo,
		         String recognitionNo,String carEngineNo) {
		super();
		this.picAdress = picAdress;
		this.carTypeName = carTypeName;
		this.carNo = carNo;
		this.recognitionNo = recognitionNo;
		this.carEngineNo = carEngineNo;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getCusId() {
		return cusId;
	}

	public void setCusId(int cusId) {
		this.cusId = cusId;
	}

	public String getPicAdress() {
		return picAdress;
	}

	public void setPicAdress(String picAdress) {
		this.picAdress = picAdress;
	}

	public String getCarTypeName() {
		return carTypeName;
	}

	public void setCarTypeName(String carTypeName) {
		this.carTypeName = carTypeName;
	}

	public String getCarNo() {
		return carNo;
	}

	public void setCarNo(String carNo) {
		this.carNo = carNo;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public String getIsDefault() {
		return isDefault;
	}

	public void setIsDefault(String isDefault) {
		this.isDefault = isDefault;
	}


	public String getRecognitionNo() {
		return recognitionNo;
	}


	public void setRecognitionNo(String recognitionNo) {
		this.recognitionNo = recognitionNo;
	}


	public String getCarEngineNo() {
		return carEngineNo;
	}


	public void setCarEngineNo(String carEngineNo) {
		this.carEngineNo = carEngineNo;
	}


	
	
	

}
